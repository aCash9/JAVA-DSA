package JavaDSA.Stacks;

import java.util.Stack;

public class Stacking3 {
    public static void main(String[] args) {
        Stack<Integer> stack = new Stack<Integer>();
        stack.push(1);
        stack.push(2);
        stack.push(3);
        stack.push(4);
        System.out.println("size");
        System.out.println(stack.size());
    }
}
